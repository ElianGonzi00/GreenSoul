    private void botonCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearCuentaActionPerformed
        
        try{
        
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/gss_prueba2", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into clientes values(?,?,?,?,?,?)");
            
            String contraseña = txtContraseña.getText().trim();
            String contraseña1 = txtContraseña1.getText().trim();
            
            if(txtNombre.getText().length()==0 && txtApellido.getText().length()==0 && txtUsuario.getText().length()==0 && 
                    txtEmail.getText().length()==0 && txtContraseña.getText().length()==0 && txtContraseña1.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "ATENCION: Rellenar todos los campos necesarios para registrarse");
            
            }else if(txtNombre.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(txtApellido.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(txtEmail.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(txtUsuario.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(txtContraseña.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(txtContraseña1.getText().length()==0){
                
                JOptionPane.showMessageDialog(null, "Ingresar todos los campos necesarios para el registro");
            
            }else if(contraseña == null ? contraseña1 == null : contraseña.equals(contraseña1)){
            
                pst.setString(1, "0");
                pst.setString(2, txtNombre.getText().trim());
                pst.setString(3, txtApellido.getText().trim());
                pst.setString(4, txtEmail.getText().trim());
                pst.setString(5, txtUsuario.getText().trim());
                pst.setString(6, txtContraseña.getText().trim());
                pst.setString(7, txtContraseña1.getText().trim());
                pst.executeUpdate();

                txtNombre.setText("");
                txtApellido.setText("");
                txtEmail.setText("");
                txtUsuario.setText("");
                txtContraseña.setText("");

                JOptionPane.showMessageDialog(null, "Cuanta Registrada");

                this.dispose();    
                ALogin1 login = new ALogin1();
                login.setVisible(true);
                login.setResizable(false);
                login.setLocationRelativeTo(null);
            
            }else{
            
                JOptionPane.showMessageDialog(null, "Error: Las contraseñas no son iguales");
            
            }
            
        }catch(Exception e){}
        
    }//GEN-LAST:event_botonCrearCuentaActionPerformed

